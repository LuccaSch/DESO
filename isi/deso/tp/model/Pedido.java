package isi.deso.tp.model;


public class Pedido {
    private Cliente cliente;
    private int id;

    public Pedido() {
    }

    public Pedido(Cliente cliente, int id) {
        this.cliente = cliente;
        this.id = id;
    }

    public Cliente getCliente() {
        return cliente;
    }

    public void setCliente(Cliente cliente) {
        this.cliente = cliente;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
   

 
}
